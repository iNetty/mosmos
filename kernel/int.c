#include "nasmfunc.h"

void init_pic(void)
{
	//master config
	io_out8(0x20, 0x11);
	io_out8(0x21, 0x20);
	io_out8(0x21, 0x04);
	io_out8(0x21, 0x05);
	io_out8(0x21, 0xFF);

	//slave config
	io_out8(0xa0, 0x11);
	io_out8(0xa1, 0x28);
	io_out8(0xa1, 0x02);
	io_out8(0xa1, 0x01);
	io_out8(0xa1, 0xFF);
	io_out8(0x21, 0b11111100); //enable keyboard/PIT interruption
}
